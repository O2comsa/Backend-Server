أولاً: التعريفات
في هذه السياسة، وما لم يقتضي السياق خلاف ذلك يكون للمصطلحات التالية المعاني المشار إليها.
•	 “إشارتي لخدمات التدريب والترجمة“: يشير إلى موقع info@esharti.net ومؤسسة إشارتي للتدريب والترجمة ، بالمملكة العربية السعودية.
•	“المستخدم” أو “أنت” أو “ضمير الملكية للمخاطب“: يشير إلى الشخص الذي يقوم بزيارة الموقع أو استخدامه أو طلب الاشتراك في الدورات التدريبية من خلال الموقع.
•	“المدرب” يشير إلى كل شخص متخصص في مجال تقديم الدورات التدريبية الذي يقوم الموقع بالتعاقد معه لتقديم الدورات التدريبية للمتدربين من خلال الموقع، وقد يتغير المدرب من دورة لأخرى.
•	“المتدرب” يشير إلى كل شخص يشترك في الدورات التدريبية المتاحة من خلال مؤسسة إشارتي للتدريب والترجمة.
•	“الدورات” أو “الدورات التدريبية” يشير إلى الدورات التي نقدمها للمشتركين أون لاين من خلال الموقع بالتعاون مع المدربين.
•	“السياسة” أو “سياسة الخصوصية” تشير إلى هذه الوثيقة وما تتضمنه من شروط وأحكام.
•	“البيانات” تشير إلى البيانات التي يقدمها لنا المستخدم أو التي نطلبها أو نجمعها عنه سواء كانت هذه البيانات شخصية تحدد هوية المستخدم أو كانت غير شخصية تتعلق بأنشطة أو سلوك أو أجهزة المستخدم المستخدمة في عملية الاتصال بالموقع.
 
ثانيًا: بيانات العضوية
•	يقدم لنا المستخدم بعض البيانات أثناء قيامه بتسجيل حسابه في موقع “مؤسسة إشارتي للتدريب والترجمة” والتي تشمل (الاسم الأول، الاسم الأخير، الدولة، المدينة، التخصص، الدرجة العلمية، رقم التصنيف “إن وجد“، البريد الالكتروني، الهاتف الجوال، كلمة المرور). وتستخدم هذه البيانات من جانب الموقع لأغراض توفير حساب المستخدم بالموقع وتمكينه من تسجيل الدخول إلينا، كما تستخدم هذه البيانات لأغراض تفعيل حساب المستخدم والتحقق من هويته ومدى مطابقة هذه البيانات للحقيقة.
•	تقدم بيانات تسجيل العضوية على مسئولية المستخدم وبالتالي يتحمل المستخدم المسئولية القانونية في حال كانت هذه البيانات غير دقيقة أو متعلقة بأشخاص آخرين أو أن المستخدم غير مفوض قانونًا بتقديمها للموقع، وسيقوم الموقع بإزالة أي بيانات نكتشف عدم دقتها أو قام الغير بالإبلاغ عنها بعد التحقق من جدية الإبلاغ.
ثالثًا: بيانات الاشتراك في الدورات
تشمل بيانات الاشتراك في الدورات نوعين من البيانات، بيانات يقدمها لنا المدربين المعتمدين لدينا، وبيانات يقدمها لنا المتدربين الراغبين في الاشتراك في الدورات:
 (1) البيانات التي يقدمها لنا المدربين
•	البيانات التي يزودنا بها المدرب كاسم مقدم الدورة وعنوان الدورة ومحاورها ومفرداتها وميعادها ومكان انعقادها، وقد يشمل ذلك نبذة تعريفية عن مقدم الدورة.
•	يزودنا المدرب بصورته الشخصية ويفوضنا بعرضها من خلال الإعلان عن الدورة سواء كان هذا الإعلان صورة ثابتة أو كان عبارة عن فيديو تعريفي بالدورة أو المدرب.
•	يفوضنا المدرب بالإفصاح عن بعض بياناته الشخصية لتعريف المتدربين بها كالاسم الكامل للمدرب وتخصصه ودرجته العلمية ومؤهلاته وخبراته العلمية والعملية.
 (2) البيانات التي يقدمها لنا المتدربين
•	يقدم لنا المتدربين بعض البيانات اللازمة للاشتراك في الدورات التي نتيحها من خلال الموقع كالاسم والسن والتخصص والدرجة العلمية والبريد الالكتروني ورقم الجوال وغيرها من البيانات التي قدم نطلبها المتدرب أثناء عملية التسجيل في الدورة.
•	تستخدم بيانات الاشتراك في الدورات لأغراض حجز الدورات وتفعيل اشتراك المتدرب في الدورة المقدمة من خلال مدير النظام التابع مؤسسة إشارتي للتدريب والترجمة.
خامسًا: تقارير الاستخدام والتتبع
•	يوفر الموقع إمكانية الحصول على تقارير الاستخدام والتتبع الملائمة لأنشطة المستخدم والتي تحتوي على معلومات نشاط المتدرب، فعلى سبيل المثال لا الحصر: (مؤشر التقدم للأنشطة، درجات الاختبارات إن وجدت، الأنشطة التعليمية التي تمت زيارتها ومدة البقاء فيها، عدد المحاولات في حال وجود تقييم، حالة النجاح أو الرسوب، الصفحات التي تم زيارتها، مدة البقاء في الصفحة، عدد المحاولات وغيرها).
•	يتيح موقع مؤسسة إشارتي للتدريب للمتدرب إصدار ومشاركة التقارير المشار إليها مع هيئة التخصصات الصحية بالمملكة العربية السعودية بغرض الضبط والمتابعة بشكل دوري.
•	يمكن الموقع المتدرب من عمل التقييمات أو الاختبارات لقياس ما إذا كانت معرفته متسقة مع أهداف المقرر ومخرجاته مع دعم آلية عادلة لقياس الدرجات مع إمكانية حصول المتدرب على تقييماته ومعرفة التواريخ النهائية للحصول عليها.
سادسًا: الترويج لأنشطة الموقع
•	قد نقوم بالتواصل معك لأغراض الترويج للدورات التدريبية في التخصصات التي نعتمدها والتي نعتقد أنها تثير اهتمامك، ويشمل ذلك اطلاعك على كل ما هو جديد لدينا من حيث الدورات المتاحة للحجز والعروض التي نقدمها أو المدربين المتميزين المتعاقد معهم وغيرها من أغراض الترويج لخدماتنا.
•	تستطيع في أي وقت طلب عدم التواصل معك بشأن الترويح للدورات التدريبية والخدمات الخاصة بنا ولكن في هذه الحالة ستفقد ميزة التعرف على كل جديد لدينا.

 
سابعًا: الاتصالات والإشعارات
•	قد نطلب منكم أثناء التواصل معنا تزويدنا ببعض البيانات عنكم، كما قد تقدم لنا باختيارك بعض البيانات الخاصة بك أو بالمشكلة التي تواجهك، وسيقتصر استخدام هذه البيانات على التواصل معك والرد عليك وحل المشكلات التي تواجهك.
ثامنًا: السلامة والأمان
•	يستخدم موقع مؤسسة إشارتي للتدريب معايير أمان لحماية بيانات مستخدمي الموقع وتشفير كلمات المرور لضمان حماية خصوصية بيانات المستخدم وعدم اختراقها.
•	يقوم الموقع بتخزين البيانات ضمن قواعد بيانات محمية مخصصة لحفظ بيانات مستخدمي الموقع بشكل إلكتروني، ويتم حفظ قواعد البيانات هذه على الخوادم الخاصة بنا، والتي قد تكون متواجدة خارج المملكة العربية السعودية، ويستخدم الموقع معايير أمان لا تسمح لغير الأشخاص المفوضين من جانبنا بالإطلاع على تلك البيانات.
•	في حالة اختراق بيانات المستخدم من جانب القراصنة أو مرتكبي الأعمال غير المشروعة سنقوم بإخطار المستخدم خلال 24 ساعة من علمنا باختراق تلك البيانات، وسنقوم بإخطار المستخدم عبر وسائل الاتصال التي زودنا بها.
•	يحتفظ الموقع ببيانات المستخدم للمدة اللازمة لتنفيذ الخدمات للمستخدم، وبالتالي قد نقوم بحذف البيانات في حالة تنفيذ الخدمات للمستخدم.
•	قد نقوم بحفظ بيانات المستخدم ضمن قواعد بياناتنا الالكترونية أو خلال سجلاتنا الورقية لتنفيذ الأغراض المنصوص عليها في هذه السياسة.
تاسعًا: التقييمات
قد يتيح موقع مؤسسة إشارتي للتدريب لمستخدمي الموقع تقييم الدورات التدريبية التي تم الاشتراك فيها، ويقتصر التقييم على المتدربين فقط من المشتركين في الدورة، ويستطيع المتدرب إضافة المحتوى من خلال خدمة التعليقات التي يوفرها الموقع، ويتحكم المستخدم بشكل ذاتي في المعلومات التي يقدمها من خلال التقييم، ويعلم المتدرب ويوافق على أن أي معلومات يقدمها من خلال خدمة التقييم ستكون متاحة لغيره من مستخدمي الموقع، لذا ينصح بعدم نشر أي بيانات اتصال أو أي بيانات شخصية.

عاشرًا: ملفات تعريف الارتباط (سياسة الكوكيز)
 (1) ما هي ملفات تعريف الارتباط؟
ملفات تعريف الارتباط هي ملفات نصية صغيرة تحتوي على كمية معينة من البيانات، يمكن لموقعنا إرسالها لمتصفحك، ويمكن بعد ذلك تخزينها على القرص الصلب لجهاز الكمبيوتر الخاص بك عند زيارة بعض صفحات الويب، ويمكن الوصول إليها عن طريق خادم الويب الخاص بنا، وهذه الملفات لن تضر جهازك، ويستطيع الموقع بعد ذلك استرداد ملفات تعريف الارتباط هذه، وتستخدم وفقًا للأغراض المعلنة من خلال هذه السياسة.
 (2) الكوكيز وخصوصية بياناتكم
•	لا تخزن ملفات تعريف الارتباط التي نستخدمها معلومات التعريف الشخصية الخاصة بمستخدمي الموقع، وبالتالي لا تشمل هذه الملفات أي معلومات خاصة بشخص المستخدم كالاسم وأرقام الهوية ووسائل الاتصال الخاصة بالمستخدم وغيرها من البيانات التي يمكن أن تحدد بشكل مباشر أو غير مباشر هوية المستخدم.
•	ملفات تعريف الارتباط لا تجمع أي بيانات شخصية مخزنة على القرص الصلب لجهاز الكمبيوتر الخاص بكم.
(3) أغراض الكوكيز
•	تساعدنا الكوكيز بأن يكون موقعنا غني بالمعلومات وسهل الاستخدام قدر الإمكان، وتساعدنا الكوكيز على تحقيق هذا الهدف.
•	تسمح لنا ملفات تعريف الارتباط بتخصيص صفحات الويب والخدمات التي نقدمها لمستخدمي الموقع.
•	نستخدم ملفات تعريف الارتباط لأغراض التحليل من أجل تحسين تجربة التصفح باستمرار على موقعنا على الويب.
•	يساعدنا الشركاء المعتمدين من جانبنا من خلال ملفات تعريف الارتباط على تقديم الإعلانات والتحليلات من خلال الموقع.
•	تستخدم ملفات تعريف الارتباط بغرض تقديم المعلومات، فعلى سبيل المثال في موقع “مؤسسة إشارتي للتدريب” تساعدنا بعض ملفات تعريف الارتباط التي نستخدمها على تحديد الأخطاء وإصلاحها.
•	تساعد بعض ملفات تعريف الارتباط على تذكر الصفحات والنماذج التي تشاهدها عند زيارتك لموقعنا.
•	تهدف ملفات تعريف الارتباط إلى تحسين تجربة موقع مؤسسة إشارتي للتدريب في كل مرة تزوره.
 (4) ملفات تعريف الارتباط للجهات الخارجية
•	لإثراء محتوى موقعنا، قد نقوم أحيانًا بتضمين محتوى فيديو من مواقع التواصل الاجتماعي الأخرى مثل YouTube أو Facebook نتيجة لذلك عند زيارة صفحة تحتوي على محتوى من مواقع خارجية، وتقدم هذه المواقع ملفات تعريف ارتباط خاصة بها لذا يجب عليك التحقق من سياسة ملفات تعريف الارتباط الخاصة بهم للحصول على مزيد من المعلومات، وأنت تعلم وتوافق على أن موقعنا لا يسيطر على هذه الملفات ولا يقدم أي ضمانات بشأنها.
•	قد نتيح من خلال موقعنا مشاركة بعض المحتوى الخاص بنا على مواقع أخرى خارجية، وقد تقوم المواقع التي ترغب في مشاركة المحتوى بها بتعيين ملفات تعريف ارتباط عند تسجيل دخولك إلى خدماتهم، وأنت تعلم وتوافق على أن موقعنا لا يسيطر على هذه الملفات، لذا يجب عليك التحقق من السياسات التي تتبعها هذه المواقع.
 (5) الموافقة
•	باستخدامك الموقع فإنك توافق على استخدام ملفات تعريف الارتباط والتقنيات الأخرى المشابهة على النحو المنصوص عليه في هذه السياسة.
•	إذا كنت لا توافق على سياسة الكوكيز التي نعتمدها فيجب عليك التوقف فورًا عن استخدام الموقع.
 (6) التحكم في ملفات تعريف الارتباط
•	نحن نقدر أن بعض المستخدمين قد يفضلون المزيد من التحكم الفردي في زيارتهم لموقعنا، ويمكنهم ضبط إعداداتهم وفقًا لذلك، ونوضح لمستخدمينا كيفية التحكم وحذف ملفات تعريف الارتباط، فيستطيع المستخدم تغيير تفضيلات ملفات تعريف الارتباط من خلال الدخول إلى إعدادات المتصفح الذي يستخدمه في زيارة موقعنا واختيار الإعدادات المناسبة لاستخدامه لخدماتنا.
•	إذا اخترت تعطيل أو رفض أو حظر ملفات تعريف الارتباط الخاصة بنا، فإن بعض أجزاء موقعنا لن تعمل بشكل كامل أو في بعض الحالات لن يكون موقع الويب الخاص بنا متاحًا على الإطلاق
حادي عشر: النطاق العام لاستخدام البيانات
تستخدم بيانات المستخدم للأغراض المنصوص عليها في هذه السياسة إلى جانب أي من الأغراض الآتية:
•	تستخدم بيانات الاشتراك في الدورات إلى توفير جميع المعلومات التي يحتاجها المتدرب عن النشاط المقدم مثل عنوان النشاط، والأهداف التعليمية وجدول المحتويات، ونبذة عن المتحدثين والمتطلبات التقنية اللازمة لتشغيل النشاط والتقييمات السابقة من قبل المتدربين للنشاط إن وجدت .
•	تستخدم البيانات لتمكين المتدرب من استخدام خدمات موقع “مؤسسة إشارتي للتدريب” على النحو الأمثل، وتقديم الأفضل لك دائمًا.
•	تستخدم البيانات لأغراض معالجة البيانات التي تقدمها عبر خدماتنا كالتأكد من أن بريدك الالكتروني أو رقم هاتفك نشط وصالح للاستخدام ومملوك لك.
•	تستخدم البيانات لتلقي الأسئلة والشكاوى والاستفسارات من مستخدمي الموقع، والرد عليها.
•	تستخدم البيانات لمعالجة إجابات المستخدم على الاستبيانات المطروحة عبر الموقع والتي شارك المستخدم في الإجابة عليها.
•	تستخدم البيانات لتزويد المستخدم بالمعلومات عن الخدمات والدورات التي يطلبها من خلال موقع “ومؤسسة إشارتي للتدريب“.
•	تستخدم البيانات لأغراض العمل الداخلية مثل تحسين خدماتنا.
•	تستخدم البيانات للاتصال بك بشأن الاتصالات الحكومية، ووفقًا لتقديرنا الخاص، بخصوص التغييرات في سياسة الخصوصية أو شروط الاستخدام أو أي من سياستنا الأخرى.
•	تستخدم البيانات للامتثال للالتزامات التنظيمية والقانونية، كذلك للأغراض التي يتم الكشف عنها في الوقت الذي تقدم فيه معلوماتك، بموافقتك، وطبقًا لسياسة الخصوصية هذه.
 
